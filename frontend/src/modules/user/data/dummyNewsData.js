// Function to get news by category
export const getNewsByCategory = (category, district = null) => {
  let filtered;
  
  // If category is ब्रेकिंग, show only news with isBreaking: true (breaking is not a specific category)
  if (category === 'ब्रेकिंग') {
    filtered = dummyNewsData.filter(news => news.isBreaking === true);
  } else {
    filtered = dummyNewsData.filter(news => news.category === category);
  }
  
  // If category is राजस्थान and district is selected, filter by district
  if (category === 'राजस्थान' && district && district !== 'सभी जिले') {
    filtered = filtered.filter(news => news.district === district);
  }
  
  return filtered;
};

// Function to get news by ID
export const getNewsById = (id) => {
  return dummyNewsData.find(news => news.id === parseInt(id));
};

